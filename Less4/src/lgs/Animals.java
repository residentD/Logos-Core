package lgs;

public class Animals {
	
	private String type;
	private int age;
	private int speed;
	
	
	public Animals(String type, int age, int speed) {
		this.type = type;
		this.age = age;
		this.speed = speed;		
	}


	public String getType() {
		return type;
	}


	public void setType(String type) {
		this.type = type;
	}


	public int getAge() {
		return age;
	}


	public void setAge(int age) {
		this.age = age;
	}


	public int getSpeed() {
		return speed;
	}


	public void setSpeed(int speed) {
		this.speed = speed;
	}
	
	
	

}
